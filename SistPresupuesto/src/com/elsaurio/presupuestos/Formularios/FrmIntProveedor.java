    private void muestraProveedor() {
        jTextFieldNombre.setText(unProveedor.getNombre());
        jTextFieldDireccion.setText(unProveedor.getDomicilio());
        jTextFieldTelefono.setText(unProveedor.getTelefono());
        jTextFieldCelular.setText(unProveedor.getCelular());
        jTextFieldEmail.setText(unProveedor.getEmail());
    }

    
    
