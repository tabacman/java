    private void jButtonGrabaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonGrabaActionPerformed
        // TODO add your handling code here:
        unProveedor.setNombre(jTextFieldNombre.getText());
        unProveedor.setDomicilio(jTextFieldDireccion.getText());
        unProveedor.setTelefono(jTextFieldTelefono.getText());
        unProveedor.setCelular(jTextFieldCelular.getText());
        unProveedor.setEmail(jTextFieldEmail.getText());
        if (0 == unProveedor.getId()) {
            daoProveedor.agregaProveedor(unProveedor);
        } else {
            daoProveedor.modificaProveedor(unProveedor);
        };
    }//GEN-LAST:event_jButtonGrabaActionPerformed

